package basicsprograms;

public class MathClass
{

	public static void main(StringFunction[] args)
	{
		System.out.println(Math.addExact(45, 55)); 
		System.out.println(Math.subtractExact(100, 10)); 
		System.out.println(Math.multiplyExact(10, 20));
		System.out.println(Math.abs(-90));
		System.out.println(Math.max(45, 24));
		System.out.println(Math.min(54, 24));	
		System.out.println(Math.random());
		double d1 = Math.PI;
		System.out.println(d1);
		System.out.println();
		System.out.println();
		System.out.println();
	}

}
